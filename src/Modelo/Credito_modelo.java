
package Modelo;

public class Credito_modelo {
    
    private int idCredito;
    private String cedula;
    private String nombre;
    private String apellido;
    private String monto;
    private String cant_cuota;
    private String cuotamensual;

    public int getIdCredito() {
        return idCredito;
    }

    public void setIdCredito(int idCredito) {
        this.idCredito = idCredito;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getMonto() {
        return monto;
    }

    public void setMonto(String monto) {
        this.monto = monto;
    }

    public String getCant_cuota() {
        return cant_cuota;
    }

    public void setCant_cuota(String cant_cuota) {
        this.cant_cuota = cant_cuota;
    }

    public String getCuotamensual() {
        return cuotamensual;
    }

    public void setCuotamensual(String cuotamensual) {
        this.cuotamensual = cuotamensual;
    }

  
   
}
